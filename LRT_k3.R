fun1<-function(n1,n2,n3,s1,s2,s3)
{
  g1=rexp(n1,rate=1/s1)
  g2=rexp(n2,rate=1/s2)
  g3=rexp(n3,rate=1/s3)
  #q=n1/s1+n2/s2+n3/s3
  y1=min(g1);y2=min(g2);y3=min(g3)
  y11=min(y1,y2,y3);y12=min(y2,y3)
  #s11=y11-(1/q);s12=y12-(1/q);s13=y3-(1/q)
  sig10=sum(g1-y11)/n1;sig20=sum(g2-y11)/n2;sig30=sum(g3-y11)/n3
  sig1=sum(g1-y11)/n1;sig2=sum(g2-y12)/n2;sig3=sum(g3-y3)/n3
  neu=(sig1^n1)*(sig2^n2)*(sig3^n3)
  dno=(sig10^n1)*(sig20^n2)*(sig30^n3)
  lamda=neu/dno
  return(lamda)
}
fun2<-function(n1,n2,n3,s1,s2,s3)
{
  x<-replicate(1000,fun1(n1,n2,n3,s1,s2,s3))
  y<-sort(x,decreasing=FALSE)
  c<-y[50]
  return(c)
}

fun3<-function(n1,n2,n3,mu1,mu2,mu3,v1,v2,v3)
{
  g1=mu1+rexp(n1,rate=1/v1)
  g2=mu2+rexp(n2,rate=1/v2)
  g3=mu3+rexp(n3,rate=1/v3)
  m1=mean(g1);m2=mean(g2);m3=mean(g3)
  #q=n1/sqrt(v1)+n2/sqrt(v2)+n3/sqrt(v3)
  y1=min(g1);y2=min(g2);y3=min(g3)
  y11=min(y1,y2,y3);y12=min(y2,y3)
  #s11=y11-(1/q);s12=y12-(1/q);s13=y3-(1/q)
  sig10=sum(g1-y11)/n1;sig20=sum(g2-y11)/n2;sig30=sum(g3-y11)/n3
  sig1=sum(g1-y11)/n1;sig2=sum(g2-y12)/n2;sig3=sum(g3-y3)/n3
  #sig1=sum(g1-s11)/n1;sig2=sum(g2-s12)/n2;sig3=sum(g3-s13)/n3
  neu=(sig1^n1)*(sig2^n2)*(sig3^n3)
  dno=(sig10^n1)*(sig20^n2)*(sig30^n3)
  lamda=neu/dno
  s1=(n1/(n1-1))*(m1-y1);s2=(n2/(n2-1))*(m2-y2);s3=(n3/(n3-1))*(m3-y3)
  #s1=var(g1)
  out=fun2(n1,n2,n3,s1,s2,s3)
  a=0
  if(lamda<out)
    a=a+1
  return(a)
  #return(lamda)
}
fun4<-function(n1,n2,n3,mu1,mu2,mu3,v1,v2,v3)
{
  out<-replicate(5000,fun3(n1,n2,n3,mu1,mu2,mu3,v1,v2,v3))
  #plot(out)
  p<-sum(out)/5000
  return(p)
}
